package com.example.GUI.enums;

/**
 * Represents the contrast level used for styling panels in the GUI.
 * <p>
 * The values indicate progressively stronger visual contrast options.
 * </p>
 */
public enum PanelContrast {
    /** Low-contrast styling. */
    LOW,

    /** Medium-contrast styling. */
    MEDIUM,

    /** High-contrast styling. */
    HIGH
}